/** Get achievement by ID */
export function getAchievementById(id: string): AchievementData | undefined {
  return ALL_ACHIEVEMENTS.find(a => a.id === id);
}

/** Get achievements by category */
export function getAchievementsByCategory(category: AchievementCategory): AchievementData[] {
  return ALL_ACHIEVEMENTS.filter(a => a.category === category);
}

/** Get achievements by rarity */
export function getAchievementsByRarity(rarity: AchievementRarity): AchievementData[] {
  return ALL_ACHIEVEMENTS.filter(a => a.rarity === rarity);
}

/** Get visible achievements (non-hidden or unlocked) */
export function getVisibleAchievements(unlockedIds: string[]): AchievementData[] {
  return ALL_ACHIEVEMENTS.filter(a => !a.hidden || unlockedIds.includes(a.id));
}

/** Get hidden achievements count */
export function getHiddenAchievementsCount(): number {
  return ALL_ACHIEVEMENTS.filter(a => a.hidden).length;
}

/** Calculate category progress */
export function getCategoryProgress(category: AchievementCategory, unlockedIds: string[]): {
  total: number;
  unlocked: number;
  percent: number;
} {
  const categoryAchievements = getAchievementsByCategory(category);
  const unlocked = categoryAchievements.filter(a => unlockedIds.includes(a.id)).length;
  return {
    total: categoryAchievements.length,
    unlocked,
    percent: Math.round((unlocked / categoryAchievements.length) * 100),
  };
}

/** Calculate total XP from achievements */
export function getTotalAchievementXP(unlockedIds: string[]): number {
  return ALL_ACHIEVEMENTS
    .filter(a => unlockedIds.includes(a.id))
    .reduce((sum, a) => sum + a.xpReward, 0);
}

/** Get recently unlockable achievements based on current progress */
export function getCloseToUnlocking(
  unlockedIds: string[],
  stats: {
    lessonsCompleted?: number;
    streak?: number;
    wordsLearned?: number;
    perfectScores?: number;
    xp?: number;
  },
  limit = 5
): AchievementData[] {
  const locked = ALL_ACHIEVEMENTS.filter(a => !unlockedIds.includes(a.id) && !a.hidden);
  
  const withProgress = locked.map(a => {
    let progress = 0;
    let target = a.condition.value;
    
    switch (a.condition.type) {
      case "lesson_complete":
        progress = stats.lessonsCompleted || 0;
        break;
      case "streak":
        progress = stats.streak || 0;
        break;
      case "words_learned":
        progress = stats.wordsLearned || 0;
        break;
      case "perfect_score":
        progress = stats.perfectScores || 0;
        break;
      case "xp":
        progress = stats.xp || 0;
        break;
      default:
        progress = 0;
    }
    
    return {
      achievement: a,
      progressPercent: Math.min((progress / target) * 100, 100),
    };
  });
  
  return withProgress
    .filter(p => p.progressPercent > 0 && p.progressPercent < 100)
    .sort((a, b) => b.progressPercent - a.progressPercent)
    .slice(0, limit)
    .map(p => p.achievement);
}
