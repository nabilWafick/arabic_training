export type AchievementCategory = 'learning' | 'social' | 'mastery' | 'special' | 'legendary';
export type AchievementRarity = 'common' | 'uncommon' | 'rare' | 'epic' | 'legendary';

export interface AchievementCondition {
  type: 
    | 'lesson_complete' 
    | 'phase_complete' 
    | 'streak' 
    | 'xp' 
    | 'perfect_score' 
    | 'exercises' 
    | 'time_spent'
    | 'words_learned'
    | 'time_of_day'
    | 'speed_complete'
    | 'social_action'
    | 'special'
    | 'cefr_level'
    | 'weekly_xp'
    | 'consecutive_perfect'
    | 'languages_used';
  value: number;
  lessonId?: string;
  phaseId?: number;
  timeStart?: number; // Hour of day (0-23)
  timeEnd?: number; // Hour of day (0-23)
  action?: string; // For social actions
}

export interface AchievementData {
  id: string;
  category: AchievementCategory;
  icon: string;
  title: string;
  titleAr: string;
  titleFr: string;
  description: string;
  descriptionAr: string;
  descriptionFr: string;
  xpReward: number;
  rarity: AchievementRarity;
  hidden?: boolean; // Hidden achievements - surprise reveals
  condition: AchievementCondition;
  hint?: string; // Hint for locked achievements
  hintAr?: string;
  hintFr?: string;
}

